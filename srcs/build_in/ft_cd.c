#include "ft_minishell.h"

int	ft_cd(t_cmd *cmd)
{
	int	res;

	errno = 0;
	res = chdir(cmd->args[1]);
	if (res == -1)
	{
		ft_printf("%s\n", strerror(errno));
		return (0);
	}
	return (1);
}
