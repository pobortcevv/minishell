#include "ft_minishell.h"

void	pwd(t_sh *sh, char **env)
{
	size_t	i;
	size_t	len;

	i = 4;
	(void)sh; // Пока основная структура не нужна
	len = ft_strlen(env[7]);
	while (i < len)
	{
		write(1, &env[7][i], 1);
		i++;
	}
	write(1, "\n", 1);
}
