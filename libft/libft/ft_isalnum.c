int		ft_isalnum(int chr)
{
	if ((chr >= '0' && chr <= '9') || (chr >= 'a' && chr <= 'z') ||
	(chr >= 'A' && chr <= 'Z'))
	{
		return (1);
	}
	return (0);
}
